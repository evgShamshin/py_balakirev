cmd = input()

match cmd:
    case "top" | "Top" | "TOP":
        print(f"Команда {cmd.lower()}")
    case "bottom" | "Bottom" | "BOTTOM":
        print(f"Команда {cmd.lower()}")
    case "right" | "Right" | "RIGHT":
        print(f"Команда {cmd.lower()}")
    case "left" | "Left" | "LEFT":
        print(f"Команда {cmd.lower()}")